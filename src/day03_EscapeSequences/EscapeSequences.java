package day03_EscapeSequences;

/*
escape sequences MUST be give with double quote
  \n: starts a new line
  \t: tab
  \\: prints one back slash
  \": double quote
 */


public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Java \nPython \nC#");

        System.out.println("--------------------");

        System.out.println("Hello Cydeo! \nHow are you today \nIt's nice top see you!\nWhat claas do we have?");

        System.out.println("---------------------");

        System.out.println("\tJava is cool Programming Language");

        System.out.println("---------------------");

        System.out.println("/ \\"); // to print one back slash type two back slashes

        System.out.println("---------------------");

        System.out.println("My favorite TV show is \"Shameless\""); // escape sequence MUST be used for double quote


    }



}
